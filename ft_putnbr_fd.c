#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (nb < 0)
	{
		ft_putchar_fd('-');
		if (nb == -2147483648)
		{
			ft_putchar_fd('2');
			ft_putnbr_fd(147483648);
		}
		nb *= -1;
	}
	if ((nb / 10) > 0)
		ft_putnbr_fd(nb / 10);
	if (nb != -2147483648)
		ft_putchar_fd(nb % 10 + '0');
}
